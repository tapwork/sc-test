#import "UIColor+Soundcloud.h"

@implementation UIColor (Soundcloud)

+ (UIColor*)soundcloudOrange
{
    return [UIColor colorWithRed:1.0 green:88.0/255.0 blue:0.0 alpha:1.0];
}

+ (UIColor*)soundcloudGray
{
    return [UIColor colorWithRed:239.0/255.0 green:239.0/255.0 blue:239.0/255.0 alpha:1.0];;
}


@end
